    private void jbtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnExcluirActionPerformed
        int option = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja deletar?");

        if (option != 0) {
            return;
        }

        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setId((int) idSelecionado);
        boolean result = true;

        try {
            fornecedorController.Excluir(fornecedor);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
            result = false;
        }

        if (result) {
            this.idSelecionado = 0;
            jbtnExcluir.setEnabled(false);

            jtbFornecedores.setEnabled(true);

            jtxtRazao.setEnabled(false);
            jtxtFantasia.setEnabled(false);
            jtxtCnpjCpf.setEnabled(false);
            jtxtInscricao.setEnabled(false);
            jtxtTelefone.setEnabled(false);

            jtxtRazao.setText("");
            jtxtFantasia.setText("");
            jtxtCnpjCpf.setText("");
            jtxtInscricao.setText("");
            jtxtTelefone.setText("");

            load();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Erro ao salvar");
        }
    }//GEN-LAST:event_jbtnExcluirActionPerformed

