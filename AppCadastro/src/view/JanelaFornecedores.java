    public void filtrarTabela(String valueFilter) {

        TableRowSorter sorter = new TableRowSorter(adapterFornecedorJTable);
        jtbFornecedores.setRowSorter(sorter);

        if (valueFilter.length() == 0) {
            sorter.setRowFilter(null);
        } else {
            try {

                sorter.setRowFilter(RowFilter.regexFilter("(?i)" + valueFilter));

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao buscar o valor!!!", "AVISO - Erro", JOptionPane.ERROR_MESSAGE);
            }
        }

    }

    private void load() {

        List<Fornecedor> fornecedores = null;
        try {

            adapterFornecedorJTable = fornecedorController.AllTable();
            jtbFornecedores.setModel(adapterFornecedorJTable);
        } catch (SQLException ex) {
            Logger.getLogger(Fornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void tableaValue() {
        int row = jtbFornecedores.getSelectedRow();
        Fornecedor fornecedor = adapterFornecedorJTable.getValue(row);
        idSelecionado = fornecedor.getId();
        jbtnEditar.setEnabled(true);
        jbtnExcluir.setEnabled(true);
        jtxtRazao.setText(fornecedor.getRazao());
        jtxtFantasia.setText("Faltando");
        jtxtCnpjCpf.setText(fornecedor.getCpf_cnpj());
        jtxtInscricao.setText(fornecedor.getRg_insc());
        jtxtTelefone.setText(fornecedor.getTelefone());
    }

    private void salvar(boolean editar) {

        String razao = jtxtRazao.getText();
        String fantasia = jtxtFantasia.getText();
        String cpfCnpj = jtxtCnpjCpf.getText();
        String incricao = jtxtInscricao.getText();
        String telefone = jtxtTelefone.getText();
        Fornecedor fornecedor = new Fornecedor((int) idSelecionado, razao, cpfCnpj, incricao, telefone, fantasia);

        boolean result = true;

        if (editar) {

            try {

                fornecedorController.Editar(fornecedor);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
                result = false;
            }
        } else {

            try {
                fornecedorController.Adicionar(fornecedor);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
                result = false;
            }
        }

        if (result) {
            idSelecionado = 0;
            jbtnNovo.setEnabled(true);
            jbtnEditar.setEnabled(false);
            jbtnExcluir.setEnabled(false);
            jbtnSalvar.setEnabled(false);
            jbtnCancelar.setEnabled(false);
            jtbFornecedores.setEnabled(true);

            jtxtRazao.setEnabled(false);
            jtxtFantasia.setEnabled(false);
            jtxtCnpjCpf.setEnabled(false);
            jtxtInscricao.setEnabled(false);
            jtxtTelefone.setEnabled(false);

            jtxtRazao.setText("");
            jtxtFantasia.setText("");
            jtxtCnpjCpf.setText("");
            jtxtInscricao.setText("");
            jtxtTelefone.setText("");

            load();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Erro ao salvar");
        }
    }

